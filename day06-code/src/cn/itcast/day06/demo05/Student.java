package cn.itcast.day06.demo05;
/*一个标准类
1.private
2.getter ，setter
3无参构造方法
4.有参构造方法
这种类也叫Java bean
 */
public class Student {
    private String name;
    private int age;

    public Student() {
    }

    public Student(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

}
